#!/bin/sh
stopContainerByName() {
	echo '================================================================================'
	echo '			Stopping all the containers'
	echo '================================================================================'
	docker stop $(docker ps -a -q)
	echo '================================================================================'

	echo '================================================================================'
	echo '			Removing all containers'
	echo '================================================================================'
	docker rm $(docker ps -a -q)
	echo '================================================================================'
}

runAll() {
	echo '================================================================================'
	echo '			run sei-us-demo container'
	echo '================================================================================'
	./scripts/sei-us-demo/run.sh &

}

stopContainerByName
runAll
